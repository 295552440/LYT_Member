function checkApply(hyname, phoneNumber, identifyId, tjrId, bankcardNumber,
		shouhuoAddress) {
	if (hyname == "") {
		alert("会员姓名不能为空");
		return false;
	}
	if (phoneNumber == "") {
		alert("手机号不能为空");
		return false;
	}
	if (phoneNumber.length != 11) {
		alert('请输入有效的手机号码！');
		return false;
	}

	var myreg0 = /^0?1[3|4|5|8][0-9]\d{8}$/;
	if (!myreg0.test(phoneNumber)) {
		alert('请输入有效的手机号码！');
		return false;
	}
	if (identifyId == "") {
		alert("身份证号不能为空");
		return false;
	}
	if (!checkIdcard(identifyId))
		return false;
	if (tjrId == "") {
		alert("推荐人卡号不能为空");
		return false;
	}
	var myreg1 = /^0?[D|G|S][C]\d{16}$/;
	if (!myreg1.test(tjrId)) {
		alert('请输入有效的会员卡号！');
		return false;
	}
	if (bankcardNumber == "") {
		alert("银行卡号不能为空");
		return false;
	}
	if (!luhmCheck(bankcardNumber))
		return false;
	if (shouhuoAddress == "") {
		alert("收货地址不能为空");
		return false;
	}
	if (!checkHycard(tjrId))
		return false;
	return true;
}

